# ============== 設定欄位寬度 ==============
COLUMN_WIDTHS = {
    'A': 12,  # 產品代號
    'B': 30,  # 產品名稱
    'C': 20,  # 規格
    'D': 12,  # 訂購數量
    'E': 10,  # 單價
    'F': 15,  # 備註
}


def get_credentials():
    """取得 Google API 認證"""
    creds = None
    
    # 檢查是否有現有的 token
    if os.path.exists(TOKEN_FILE):
        with open(TOKEN_FILE, 'rb') as token:
            creds = pickle.load(token)
    
    # 如果沒有有效的認證，則進行 OAuth 流程
    if not creds or not creds.valid:
        if creds and creds.expired and creds.refresh_token:
            creds.refresh(Request())
        else:
            if not os.path.exists(CREDENTIALS_FILE):
                print(f"❌ 錯誤：找不到認證檔案 {CREDENTIALS_FILE}")
                return None
            
            flow = InstalledAppFlow.from_client_secrets_file(
                CREDENTIALS_FILE, SCOPES)
            creds = flow.run_local_server(port=0)
        
        # 儲存 token 以供未來使用
        with open(TOKEN_FILE, 'wb') as token:
            pickle.dump(creds, token)
    
    return creds


def create_sheet():
    """創建 Google Sheet"""
    creds = get_credentials()
    if not creds:
        return None
    
    try:
        # 連接到 Google Sheets
        print("🔗 連接到 Google Sheets...")
        gc = gspread.authorize(creds)
        
        # 創建新 Spreadsheet
        print(f"📄 創建 '{SHEET_TITLE}'...")
        spreadsheet = gc.create(SHEET_TITLE)
        
        # 取得第一個工作表並重新命名
        worksheet = spreadsheet.sheet1
        worksheet.update_title(SHEET_NAME)
        
        # 寫入資料
        print(f"📝 寫入訂單資料到 '{SHEET_NAME}'...")
        worksheet.update(ORDER_DATA)
        
        # 格式化表頭（加粗、背景色）
        print("🎨 格式化表頭...")
        worksheet.format('A1:F1', {
            'textFormat': {'bold': True},
            'backgroundColor': {'red': 0.9, 'green': 0.9, 'blue': 0.9},
            'horizontalAlignment': 'CENTER'
        })
        
        # 設定欄位寬度
        print("📐 調整欄位寬度...")
        for col, width in COLUMN_WIDTHS.items():
            worksheet.format(f'{col}:{col}', {
                'horizontalAlignment': 'CENTER'
            })
        
        # 設定儲存格對齊
        worksheet.format('A2:F200', {
            'horizontalAlignment': 'CENTER'
        })
        
        # 凍結第一行
        worksheet.freeze(rows=1)
        
        # 取得網址
        sheet_url = f"https://docs.google.com/spreadsheets/d/{spreadsheet.id}"
        
        print("\n" + "="*50)
        print("✅ Google Sheet 創建成功！")
        print("="*50)
        print(f"\n📋 標題：{SHEET_TITLE}")
        print(f"📑 分頁：{SHEET_NAME}")
        print(f"\n🔗 網址：{sheet_url}")
        print("\n💡 提示：")
        print("   - 請登入 Google 帳號後開啟連結")
        print("   - 點擊右上角「分享」可設定權限")
        print("="*50)
        
        return sheet_url
        
    except gspread.exceptions.APIError as e:
        print(f"❌ Google API 錯誤：{e}")
        return None
    except Exception as e:
        print(f"❌ 發生錯誤：{e}")
        return None


def main():
    """主程式"""
    print("="*50)
    print("中聯東勢訂單 Google Sheet 創建工具")
    print("="*50)
    print()
    print("注意：第一次使用時會開啟瀏覽器進行 Google 授權")
    print()
    
    url = create_sheet()
    
    if url:
        return 0
    else:
        print("\n❌ 創建失敗，請檢查上述錯誤訊息")
        return 1


if __name__ == "__main__":
    sys.exit(main())
