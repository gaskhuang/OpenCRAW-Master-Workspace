# ============== 欄位寬度設定 ==============
COLUMN_WIDTHS = {
    'A': 12,  # 代號
    'B': 35,  # 品名
    'C': 10,  # 數量
    'D': 10,  # 單價
    'E': 15,  # 備註
}


def setup_credentials():
    """設置 Google API 認證"""
    if not os.path.exists(CREDENTIALS_FILE):
        print(f"❌ 錯誤：找不到認證檔案 {CREDENTIALS_FILE}")
        print("\n請按照 GOOGLE_SHEETS_SETUP.md 的步驟設置認證")
        return None
    
    try:
        scopes = [
            "https://www.googleapis.com/auth/spreadsheets",
            "https://www.googleapis.com/auth/drive"
        ]
        credentials = service_account.Credentials.from_service_account_file(
            CREDENTIALS_FILE, scopes=scopes
        )
        return credentials
    except Exception as e:
        print(f"❌ 認證失敗：{e}")
        return None


def create_order_sheet():
    """創建訂單 Google Sheet"""
    credentials = setup_credentials()
    if not credentials:
        return None
    
    try:
        print("🔗 連接到 Google Sheets...")
        gc = gspread.authorize(credentials)
        
        # 創建新 Spreadsheet
        print(f"📄 創建 '{SHEET_TITLE}'...")
        spreadsheet = gc.create(SHEET_TITLE)
        
        # 取得第一個工作表
        worksheet = spreadsheet.sheet1
        first_sheet = True
        
        # 為每個訂單創建分頁
        for order_name, order_data in ORDERS.items():
            if first_sheet:
                # 第一個分頁直接重新命名
                ws = worksheet
                ws.update_title(order_name)
                first_sheet = False
            else:
                # 新增分頁
                ws = spreadsheet.add_worksheet(title=order_name, rows="100", cols="10")
            
            print(f"📝 寫入訂單 '{order_name}'...")
            ws.update(order_data)
            
            # 格式化表頭
            ws.format('A1:E1', {
                'textFormat': {'bold': True},
                'backgroundColor': {'red': 0.9, 'green': 0.9, 'blue': 0.9},
                'horizontalAlignment': 'CENTER'
            })
            
            # 設定欄位寬度
            for col, width in COLUMN_WIDTHS.items():
                ws.format(f'{col}:{col}', {
                    'horizontalAlignment': 'CENTER'
                })
        
        # 設定儲存格對齊
        for order_name in ORDERS.keys():
            ws = spreadsheet.worksheet(order_name)
            ws.format('A2:E100', {
                'horizontalAlignment': 'CENTER'
            })
        
        # 取得網址
        sheet_url = f"https://docs.google.com/spreadsheets/d/{spreadsheet.id}"
        
        print("\n" + "="*50)
        print("✅ Google Sheet 創建成功！")
        print("="*50)
        print(f"\n📋 標題：{SHEET_TITLE}")
        print(f"📑 分頁：{', '.join(ORDERS.keys())}")
        print(f"\n🔗 網址：{sheet_url}")
        print("\n💡 提示：")
        print("   - 請登入 Google 帳號後開啟連結")
        print("   - 點擊右上角「分享」可設定權限")
        print("="*50)
        
        return sheet_url
        
    except Exception as e:
        print(f"❌ 發生錯誤：{e}")
        return None


def main():
    """主程式"""
    print("="*50)
    print("百佳訂單 Google Sheet 創建工具")
    print("="*50)
    print()
    
    url = create_order_sheet()
    
    if url:
        return 0
    else:
        print("\n❌ 創建失敗，請檢查上述錯誤訊息")
        return 1


if __name__ == "__main__":
    sys.exit(main())
